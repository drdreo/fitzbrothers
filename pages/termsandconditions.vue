<script lang="ts">
  import Vue from 'vue';
  import Component from 'vue-class-component';

  @Component({
    head() {
      return {
        title: 'Fitz Brothers (official) | Terms & Conditions'
      };
    }
  })
  export default class TermsAndConditions extends Vue {

    errors: string[] = [];
    name = '';
    email = '';
    subject = '';
    message = '';

    checkForm(e: Event) {
      e.preventDefault();

      this.errors = [];

      if (!this.name) {
        this.errors.push('Name is required.');
      }

      if (!this.email) {
        this.errors.push('Email is required to get an answer.');
      } else if (!this.validEmail(this.email)) {
        this.errors.push('Email is not valid.');
      }

      if (!this.subject) {
        this.errors.push('Please enter a subject you want to talk about.');
      }
      if (!this.message) {
        this.errors.push('Please enter your message.');
      }

      if (!this.errors.length) {
        const data = { name: this.name, email: this.email, subject: this.subject, message: this.message };
        fetch('/ajax/ajax_contact_mail.php', { method: 'POST', body: JSON.stringify(data) })
          .then(async res => {
            if (res.status === 204) {
              console.log('Email has been sent successfully!');
            } else if (res.status === 400) {
              let errorResponse = await res.json();
              this.errors.push(errorResponse.error);
            }
          });
      }
    }

    validEmail(email: string) {
      const re = /^(([^<>()[\]\\.,;:\s@"]+(\.[^<>()[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/;
      return re.test(email);
    }

  }
</script>

